def add(num1, num2):
    print("{0} + {1} = {2}".format(num1, num2, num1 + num2))

def substract(num1, num2):
    print("{0} - {1} = {2}".format(num1, num2, num1 - num2))

def multiple(num1, num2):
    print("{0} x {1} = {2}".format(num1, num2, num1 * num2))

def division(num1, num2):
    print("{0} / {1} = {2}".format(num1, num2, int(num1 / num2)))