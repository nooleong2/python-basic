# 숫자 자료형
print(5)
print(-10)
print(3.14)
print(1000)
print(5 + 3)
print(2 * 8)
print(3 * (3 + 1))

# 문자 자료형
print('풍선')
print("나비")
print("하하하하하하하")
print("ㅋ" * 8)

# boolean 자료형 (참, 거짓)
print(5 > 10)
print(5 < 10)
print(True)
print(False)
print(not True) #  not 반대를 의미
print(not False)
print(not (5 > 10)) # 5는 10보다 크다 거짓이지만 not이 존재하기 때문에 True
